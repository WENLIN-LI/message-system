const humanizeTranslationKey = (key: string) => {
  const lastSegment = key.split('.').pop() || key;

  return lastSegment
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/[-_]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .replace(/^./, (char) => char.toUpperCase());
};

const completeResourcesWithFallback = (
  source: Record<string, { translation: Record<string, string> }>,
  fallbackLanguage: string,
) => {
  const fallbackTranslation = source[fallbackLanguage]?.translation ?? {};
  const missingByLanguage: Record<string, string[]> = {};

  const completed = Object.fromEntries(
    Object.entries(source).map(([language, resource]) => {
      const missingKeys = Object.keys(fallbackTranslation).filter(
        (key) => !(key in resource.translation),
      );

      if (missingKeys.length > 0) {
        missingByLanguage[language] = missingKeys;
      }

      return [
        language,
        {
          translation: {
            ...fallbackTranslation,
            ...resource.translation,
          },
        },
      ];
    }),
  );

  if (import.meta.env.DEV && Object.keys(missingByLanguage).length > 0) {
    console.warn('[i18n] Missing locale keys filled from English fallback:', missingByLanguage);
  }

  return completed;
};

const completedResources = completeResourcesWithFallback(resources, FALLBACK_LANGUAGE);

// 初始化i18n
i18n
  .use(LanguageDetector) // 自动检测用户语言
  .use(initReactI18next) // 传递i18n到react-i18next
  .init({
    resources: completedResources,
    fallbackLng: FALLBACK_LANGUAGE, // 如果检测失败，使用英语
    returnNull: false,
    returnEmptyString: false,
    saveMissing: import.meta.env.DEV,
    missingKeyHandler: (languages, namespace, key) => {
      if (import.meta.env.DEV) {
        console.warn('[i18n] Missing translation key:', { languages, namespace, key });
      }
    },
    parseMissingKeyHandler: humanizeTranslationKey,
    interpolation: {
      escapeValue: false // 不转义HTML
    },
    detection: {
      order: ['localStorage', 'navigator'],
      caches: ['localStorage']
    }
  });

export default i18n;
